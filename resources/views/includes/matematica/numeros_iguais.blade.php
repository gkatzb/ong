<script>
    $(document).ready(function () {
        var erros = 0;
        var acertos = 0;
        var countRowAcertos = 0;

        $('.img-atividade-num').on('click', function () {
            var rowId = $(this).closest('table').attr('id');
            var imgId = $(this).text();

            if (rowId == imgId) {
                countRowAcertos += countRowAcertos++ +1;
                console.log(countRowAcertos);
                if(countRowAcertos >= 2) {
                    $(this).closest('tr').find('.icon-atv-wait').addClass('hidden');
                    $(this).closest('tr').find('.icon-atv-check').removeClass('hidden');
                    countRowAcertos = 0;
                }

                if(acertos < 8){
                    $("#acertos-1").val(acertos++ + 1);
                }
                swal("Parabéns, resposta certa!", "", "success");
                playSound('claps');
                $(this).addClass('acerto');
            } else {
                $("#erros-1").val(erros++ + 1);
                swal("Ops... Resposta errada!", "", "warning");
                playSound('wrong');
                $(this).addClass('erro');
            }
            $(this).unbind('click');
            $(this).css('cursor', 'auto');
            $(this).attr('disabled', true);
        });

        $('.btn-atividade-1').on('click', function (e) {
            var sbtvId = $("#hdn_subatividade_id-1").val();
            var toHide = '';
            var toShow = '';
            var erros = $("#erros-1").val();
            var acertos = $("#acertos-1").val();

            minTry(sbtvId, 4, acertos, erros, toHide, toShow, 1);
        });

        $('#btn-prev-1').on('click', function () {
            var toHide = '';
            var toShow = '';
            btnPrevAtvd(toHide, toShow);
        });
    });
</script>