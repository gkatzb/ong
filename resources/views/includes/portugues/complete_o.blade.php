<script>
    $(document).ready(function () {

        var $erros = 0;
        var $acertos = 0;
        var $dateIni = getDate();

        $('.btn-atividade-12').on('click', function(e){
            if($acertos < 28) {
                swal("Ops... Alguma resposta está errada!", "", "warning");
                playSound('wrong');
            }
            else if ($acertos >= 28){
                playSound('claps');
                $(e).addClass('no-click');
                $(e).attr('readonly', 'true');
                $(e).unbind('click');
                $(e).addClass('acerto');
                $(e).parent('table').parent('div').find('.icon-atv-error').addClass('hidden');
                $(e).parent('table').parent('div').find('.icon-atv-check').removeClass('hidden');
                swal({
                            title: "Parabéns, você acertou todas as respostas!",
                            type: "success",
                            showCancelButton: false,
                            closeOnConfirm: true
                        },
                        function (isConfirm) {
                            if (isConfirm) {
                                var sbtvId = $("#hdn_subatividade_id-12").val();
                                var toHide = '.subatividade-12';
                                var toShow = '.subatividade-13';
                                var erros = $("#erros-12").val();
                                var acertos = $("#acertos-12").val();
                                var dateFim = getDate();
                                var no_errors = false;

                                if(erros == 0){
                                    no_errors = true;
                                }

                                minTry(sbtvId, 28, acertos, erros, toHide, toShow, 12, $dateIni, dateFim, no_errors);
                            }
                        });
            }
        });

        $('.task_input').on('keyup', function(e){
            if(!$(this).is('[readonly]')){
                var letra = $(this).attr('id');
                if($(this).val().toUpperCase() == letra){
                    if(!$(this).hasClass('acerto-tbl')){
                        $("#acertos-12").val($acertos++);
                    }
                    $(this).addClass('no-click');
                    $(this).attr('readonly', 'true');
                    $(this).unbind('click');
                    $(this).addClass('acerto-tbl');
                    if(!$(this).parent('table').find('.task_input').hasClass('erro-tbl')){
                        $(this).parent('table').parent('div').find('.icon-atv-error').addClass('hidden');
                        $(this).parent('table').parent('div').find('.icon-atv-check').removeClass('hidden');
                    }
                    playSound('claps');
                    swal({
                        title: "Parabéns, a resposta está correta!",
                        type: "success",
                        showCancelButton: false,
                        closeOnConfirm: true
                    });

                } else if($(this).val().toUpperCase() != letra){
                    swal("Ops... Resposta errada!", "", "warning");
                    playSound('wrong');
                    $("#erros-12").val($erros++);
                    $(this).addClass('erro-tbl');
                    $(this).parent('table').parent('div').find('.icon-atv-check').addClass('hidden');
                    $(this).parent('table').parent('div').find('.icon-atv-error').removeClass('hidden');
                }
            }
        });

        $('#btn-prev-9').on('click', function(e){
            var toHide = '.subatividade-12';
            var toShow = '.subatividade-13';
            btnPrevAtvd(toHide, toShow);
        });
    });
</script>