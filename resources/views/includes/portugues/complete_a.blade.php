<script>
    $(document).ready(function () {

        var erros = 0;
        var acertos = 0;
        var $dateIni = getDate();

        $('.btn-atividade-9').on('click', function(e){
            $('.task_input-a').each(function(e){
                if(!$(this).is('[readonly]')){
                    var letra = $(this).attr('id');
                    if($(this).val().toUpperCase() == letra){
                        if(!$(this).hasClass('acerto-tbl')){
                            $("#acertos-9").val(acertos++);
                        }
                        $(this).addClass('no-click');
                        $(this).attr('readonly', 'true');
                        $(this).unbind('click');
                        $(this).addClass('acerto-tbl');
                        if(!$(this).parent('table').find('.task_input-a').hasClass('erro-tbl')){
                            $(this).parent('table').parent('div').find('.icon-atv-error').addClass('hidden');
                            $(this).parent('table').parent('div').find('.icon-atv-check').removeClass('hidden');
                        }
                    } else if($(this).val().toUpperCase() != letra){
                        if($(this).val() != ''){
                            swal("Ops... Alguma resposta está errada!", "", "warning");
                            playSound('wrong');
                            erros = erros+1;
                            $("#erros-9").val(erros);
                            $(this).addClass('erro-tbl');
                            $(this).parent('table').parent('div').find('.icon-atv-check').addClass('hidden');
                            $(this).parent('table').parent('div').find('.icon-atv-error').removeClass('hidden');
                        } else if($(this).val() == ''){
                            swal("Ops... Atividade incompleta!", "", "warning");
                            playSound('wrong');
                        }
                    }
                }
            });
            if (acertos >= 33){
                playSound('claps');
                swal({
                    title: "Parabéns, você acertou todas as respostas!",
                    type: "success",
                    showCancelButton: false,
                    closeOnConfirm: true
                    },
                    function (isConfirm) {
                        if (isConfirm) {
                            var sbtvId = $("#hdn_subatividade_id-9").val();
                            var toHide = '.subatividade-9';
                            var toShow = '.subatividade-10';
                            var erros = $("#erros-9").val();
                            var acertos = $("#acertos-9").val();
                            var dateFim = getDate();
                            var no_errors = false;

                            if(erros == 0){
                                no_errors = true;
                            }
                            minTry(sbtvId, 32, acertos, erros, toHide, toShow, 5, $dateIni, dateFim, no_errors);
                        }
                });
            }
        });

        $('#btn-prev-9').on('click', function(e){
            var toHide = '.subatividade-9';
            var toShow = '';
            btnPrevAtvd(toHide, toShow);
        });
    });
</script>