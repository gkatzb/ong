<script>
    $(document).ready(function () {

        var $erros = 0;
        var $acertos = 0;
        var $dateIni = getDate();

        $('.btn-atividade-10').on('click', function(e){
            if(parseInt($("#acertos-10").val()) < 34) {
                swal("Ops... Alguma resposta está errada!", "", "warning");
                playSound('wrong');
            }
            else if (parseInt($("#acertos-10").val()) >= 34){
                playSound('claps');
                $(e).addClass('no-click');
                $(e).attr('readonly', 'true');
                $(e).unbind('click');
                $(e).addClass('acerto');
                $(e).parent('table').parent('div').find('.icon-atv-error').addClass('hidden');
                $(e).parent('table').parent('div').find('.icon-atv-check').removeClass('hidden');
                swal({
                            title: "Parabéns, você acertou todas as respostas!",
                            type: "success",
                            showCancelButton: false,
                            closeOnConfirm: true
                        },
                        function (isConfirm) {
                            if (isConfirm) {
                                var sbtvId = $("#hdn_subatividade_id-10").val();
                                var toHide = '.subatividade-10';
                                var toShow = '.subatividade-11';
                                var erros = $("#erros-10").val();
                                var acertos = $("#acertos-10").val();
                                var dateFim = getDate();
                                var no_errors = false;

                                if(erros == 0){
                                    no_errors = true;
                                }

                                minTry(sbtvId, 34, acertos, erros, toHide, toShow, 5, $dateIni, dateFim, no_errors);
                            }
                        });
            }
        });

        $('.task_input-e').on('keyup', function(e){
            if(!$(this).is('[readonly]')){
                var letra = $(this).attr('id');
                if($(this).val().toUpperCase() == letra){
                    if(!$(this).hasClass('acerto-tbl')){
                        $("#acertos-10").val($acertos++);
                    }
                    $(this).addClass('no-click');
                    $(this).attr('readonly', 'true');
                    $(this).unbind('click');
                    $(this).addClass('acerto-tbl');
                    if(!$(this).parent('table').find('.task_input-e').hasClass('erro-tbl')){
                        $(this).parent('table').parent('div').find('.icon-atv-error').addClass('hidden');
                        $(this).parent('table').parent('div').find('.icon-atv-check').removeClass('hidden');
                    }
                    playSound('claps');
                    swal({
                        title: "Parabéns, a resposta está correta!",
                        type: "success",
                        showCancelButton: false,
                        closeOnConfirm: true
                    });

                } else if($(this).val().toUpperCase() != letra){
                    swal("Ops... Resposta errada!", "", "warning");
                    playSound('wrong');
                    $("#erros-10").val($erros++);
                    $(this).addClass('erro-tbl');
                    $(this).parent('table').parent('div').find('.icon-atv-check').addClass('hidden');
                    $(this).parent('table').parent('div').find('.icon-atv-error').removeClass('hidden');
                }
            }
        });

        $('#btn-prev-10').on('click', function(e){
            var toHide = '.subatividade-10';
            var toShow = '.subatividade-9';
            btnPrevAtvd(toHide, toShow);
        });
    });
</script>